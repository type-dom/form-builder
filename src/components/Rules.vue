              <p class="smart-fs-h3">第二十条　养犬不得危害公共利益，不得干扰他人正常生活，不得放任、驱使犬只恐吓、伤害他人。</p>
              <p class="smart-fs-h3">犬吠影响他人时，养犬人或者携犬人应当采取措施予以制止。</p>
