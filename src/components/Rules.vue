              <p class="smart-fs-h3">第五十一条 烈性犬、大型犬的品种和标准，由市公安部门会同市畜牧兽医主管部门确定、公布。</p>
              <p class="smart-fs-h3">第五十二条 军事管理区、军队干休所内的养犬管理工作，由军队有关部门按照本条例的有关规定负责。</p>
