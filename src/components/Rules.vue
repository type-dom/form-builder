              <p class="smart-fs-h3">第二十四条　重点管理区养犬还应当遵守下列规定：</p>
              <p class="smart-fs-h3">(一)个人养犬每户限养一只，不得饲养烈性犬、大型犬。</p>
              <p class="smart-fs-h3">(二)不得在禁止遛犬的区域和时间内遛犬。</p>
