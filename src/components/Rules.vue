              <p class="smart-fs-h3">重点管理区犬只产幼犬的，养犬人应当向居住地公安派出所备案，并自幼犬出生之日起九十日内，将超过限养数量的犬只转让他人或者送交犬只留检、收留场所。</p>
              <p class="smart-fs-h3">第二十六条　重点管理区养犬应当按年度缴纳养犬管理费。具体标准由市人民政府制定，报省有关部门批准后公布。</p>
